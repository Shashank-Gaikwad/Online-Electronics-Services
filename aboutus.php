					Web-based electronic stores have become
more and more popular. However, there are not
many guidelines, nor theories, showing what
features of a store would work and why. This
paper develops a set of functional guidelines for
designing electronic stores and classifies them
into three categories: motivational, hygiene, and
media richness factors. An empirical study was
conducted to evaluate the relative effect of these
factors. The results show that the store design
does have an effect on consumer purchase
decision. A two-factor theory is plausible:
hygiene factors are the major concern when
consumers decide whether to shop
electronically, while motivational factors play a
key role when consumers choose among
different electronic stores. Media richness
factors are, in general, less important. The
implication of the findings is that, for a web
store to beat its electronic competitors,
providing good transactional support is the key.
If they would like to attract customers from
traditional stores, special attentions must be
paid to the hygiene factors.
